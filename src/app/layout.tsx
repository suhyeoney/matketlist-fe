import Script from 'next/script';
import './globals.css';
import { Providers } from '@store/provider';
import Header from './header';
import Footer from './footer';
import ErrorBoundary from './errorBoundary';

export const metadata = {
  title: 'Matket List',
  description: 'Generated by create next app',
}

const RootLayout = ({ children }: { children: React.ReactNode }) => {
  /*
    Naver Map과 같은 third-party script의 로딩 우선순위를 설정함으로써, 로딩 성능 향상 도모
    beforeInteractive : 해당 페이지가 상호작용하기 전에 가져오고 실행되어야 할 필요가 있는 경우 설정
  */
  return (
    <html lang="en" className="bg-black animate-showPage">
      <head>
        <meta name="viewport" content="viewport-fit=cover"></meta>
        <meta name="viewport" content="width=device-width, initial-scale=1.0, minimum-scale=1.0, maximum-scale=1.0 user-scalable=no" />
        <meta name="apple-mobile-web-app-capable" content="yes"></meta>
        <link rel='icon' href='/favicon.ico'/>
        <Script
          strategy="afterInteractive"
          src={`https://openapi.map.naver.com/openapi/v3/maps.js?ncpClientId=${process.env.NEXT_PUBLIC_NAVER_MAP_KEY}`}
        ></Script>
      </head>
      <body>
        <Providers>
          <ErrorBoundary>
            <Header />
            { children }
            <Footer />
          </ErrorBoundary>
        </Providers>
      </body>
    </html>
  )
}

export default RootLayout;

export const revalidate = 0; // fetch로 API 호출을 하지 않는 경우 외부에서 revalidate export
export const dynamic = 'force-dynamic';